/*
operadores de comparação
    *: > maior que
    *: >= maior que  ou igual a
    *: < menor que
    *: <= menor que ou igual a
    *: == igualdade(valor)
    *: === igualdade estrita (valor e tipo)
    *: != diferente (valor)
    *:!== diferente estrito (valor e tipo)

    retorno booleanos(true/false)
*/
console.log(2>5)
console.log(10>=5)
console.log(2<5)
console.log(11<=5)
console.log(5==5)//nesse não verifica o tipo, somente o valor
console.log('5'===5) //nesse verifica o tipo e o valor, é mais recomendavel utiliza-lo
console.log(5===5)
console.log(10!='10')
console.log(10!=='10')//utiliza-se este
